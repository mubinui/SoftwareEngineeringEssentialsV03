package Session01;

public class TypeCasting {
    public static void main(String[] args) {
        double x = 10.005;
        float y = 5.50f;
        int sum = (int)x+(int)y;
        double sum1 = x+y;
        System.out.println(sum);
        System.out.println(sum1);
        int a = 50;
        int b = 60;
        double mul = (a*b);
        System.out.println(mul);
    }
}
